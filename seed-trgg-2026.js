async function seed() {
  console.log(`Seeding ${PLAYERS.length} TRGG players...`);
  let inserted = 0;
  let skipped = 0;

  for (const p of PLAYERS) {
    // Upsert player record
    const nameParts = p.name.split(', ');
    const lastName = nameParts[0] || '';
    const firstName = nameParts[1] || '';

    const { data: player, error: playerErr } = await supabase
      .from('trgg_players')
      .upsert({ display_name: p.name, first_name: firstName, last_name: lastName }, { onConflict: 'display_name' })
      .select('id')
      .single();

    if (playerErr || !player) {
      console.error(`  ✗ Failed to upsert player: ${p.name}`, playerErr?.message);
      skipped++;
      continue;
    }

    // Insert round scores
    // Note: round_date is set to 2026-01-01 as placeholder.
    // Update with real dates once TRGG schedule data is available.
    const roundRows = p.scores.map((score, i) => ({
      player_id: player.id,
      round_date: '2026-01-01',
      stableford: score,
      notes: `2026 POY round ${i + 1} (seeded)`,
    }));

    const { error: roundErr } = await supabase
      .from('trgg_rounds')
      .insert(roundRows);

    if (roundErr) {
      console.error(`  ✗ Failed to insert rounds for ${p.name}:`, roundErr.message);
      skipped++;
    } else {
      console.log(`  ✓ ${p.name} — ${p.scores.length} rounds, ${p.pts} pts`);
      inserted++;
    }
  }

  console.log(`\nDone. ${inserted} players seeded, ${skipped} skipped.`);
}

